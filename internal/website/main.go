func mainIndex(w http.ResponseWriter, req *http.Request) {
	if req.URL.Path != "/" {
		renderNotFound(w, req)
		return
	}

	data := PageData{
		Title:       "go-webthings",
		Section:     SectionMain,
		Host:        req.Host,
		Elements:    mainElements(req),
		Description: "Composable Go packages for server-rendered websites: host-aware routing, typed template docs, and HTMX-friendly partial rendering that keeps HTML in charge.",
	}
	content := partial.NewID("content", "templates/page.gohtml").SetFileSystem(mainFS).SetDot(data)
	renderMainShell(w, req, http.StatusOK, data, content)
}

func mainElement(w http.ResponseWriter, req *http.Request) {
	element, ok := findElement(req.PathValue("element"))
	if !ok {
		renderNotFound(w, req)
		return
	}

	element.Link = docsElementURL(req, element.Slug)
	element.Image = mainElementImage(element.Slug)
	data := PageData{
		Title:       element.Name,
		Section:     SectionMain,
		Host:        req.Host,
		Element:     &element,
		Elements:    mainElements(req),
		Description: element.Description,
	}
	content := partial.NewID("content", "templates/page.gohtml").SetFileSystem(mainFS).SetDot(data)
	renderMainShell(w, req, http.StatusOK, data, content)
}

