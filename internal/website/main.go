func mainGenerateImage(w http.ResponseWriter, req *http.Request) {
	body, err := generator.PNG(req.URL.Query().Get("text"))
	if err != nil {
		http.Error(w, err.Error(), http.StatusInternalServerError)
		return
	}
	w.Header().Set("Content-Type", "image/png")
	w.Header().Set("Cache-Control", "no-store")
	_, _ = w.Write(body)
}

func generateImageURL(text string) string {
	return "/generate/image?text=" + url.QueryEscape(text)
}

func renderMainShell(w http.ResponseWriter, req *http.Request, status int, data PageData, content *partial.Partial) {
	data = pageDefaults(req, data)
	data.SEO = pageSEO(data)
	content.SetDot(data)
	page := partial.NewID("shell", "templates/layout.gohtml").
		SetConnector(connector.NewHTMX(nil)).
		SetFileSystem(mainFS).
		SetDot(data).
		SetContent(content)
	page.WithOOB(partial.NewID("main-navbar", "templates/navbar.gohtml").
		SetFileSystem(mainFS).
		SetDot(data).
		SetAlwaysSwapOOB(true))
	out, err := partial.RenderWithRequest(context.Background(), req, page)
	if err != nil {
		http.Error(w, err.Error(), http.StatusInternalServerError)
		return
	}

	w.Header().Set("Content-Type", "text/html; charset=utf-8")
	w.WriteHeader(status)
	_, _ = w.Write([]byte(out))
}

